package a_basics;

public class f_datatypes {
    public static void main (String args[]){
        char a = 'a';
        int b = 10452;
        byte c = 20;
        short d = 100;
        double e = 93.4;
        float f = 69.2f;
        long g = 12345;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
    }
}
